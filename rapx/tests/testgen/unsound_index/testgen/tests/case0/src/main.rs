#![feature(allocator_api)]
use unsound_index::*;
fn main() {
    // ;
    let v1 = [1i32, 106i32, -104i32];
    let v2 = Vec::from(v1);
    let v3 = IndexBox::new(v2);
    // v3: Live (1);
    let v4: &'_ IndexBox = &v3;
    let v5 = IndexBox::get(v4);
    println!("v5: {:?}",v5);
    // v5: Live (2), v3: Borrowed (1);
    let v6 = [-27i32, 39i32, -33i32];
    let v7 = Vec::from(v6);
    let v8 = IndexBox::new(v7);
    // v5: Live (2), v3: Borrowed (1), v8: Live (1);
    let v9: &'_ IndexBox = &v8;
    let v10 = IndexBox::get(v9);
    println!("v10: {:?}",v10);
    // v5: Live (2), v3: Borrowed (1), v8: Borrowed (1), v10: Live (2);
    let v11 = [-114i32, -124i32, 113i32];
    let v12 = Vec::from(v11);
    let v13 = IndexBox::new(v12);
    // v5: Live (2), v3: Borrowed (1), v8: Borrowed (1), v13: Live (1), v10: Live (2);
    let v14: &'_ IndexBox = &v13;
    let v15 = IndexBox::get(v14);
    println!("v15: {:?}",v15);
    // v8: Borrowed (1), v3: Borrowed (1), v13: Borrowed (1), v5: Live (2), v10: Live (2), v15: Live (2);
    let v16 = [-85i32, -42i32, 22i32];
    let v17 = Vec::from(v16);
    let mut v18 = IndexBox::new(v17);
    // v8: Borrowed (1), v3: Borrowed (1), v13: Borrowed (1), v18: Live (1), v5: Live (2), v10: Live (2), v15: Live (2);
    let v19: &'_ mut IndexBox = &mut v18;
    let v20 = 1024usize;
    IndexBox::set_idx(v19, v20);
    // v8: Borrowed (1), v3: Borrowed (1), v13: Borrowed (1), v18: Live (1), v5: Live (2), v10: Live (2), v15: Live (2);
    let v21: &'_ IndexBox = &v18;
    let v22 = IndexBox::get(v21);
    println!("v22: {:?}",v22);
    // v22: Live (2), v8: Borrowed (1), v3: Borrowed (1), v13: Borrowed (1), v18: Borrowed (1), v5: Live (2), v10: Live (2), v15: Live (2);
    let v23 = [-93i32, 54i32, 103i32];
    let v24 = Vec::from(v23);
    let v25 = IndexBox::new(v24);
    // v22: Live (2), v8: Borrowed (1), v3: Borrowed (1), v13: Borrowed (1), v25: Live (1), v18: Borrowed (1), v5: Live (2), v10: Live (2), v15: Live (2);
    let v26: &'_ IndexBox = &v25;
    let v27 = IndexBox::get(v26);
    println!("v27: {:?}",v27);
    // v22: Live (2), v8: Borrowed (1), v3: Borrowed (1), v13: Borrowed (1), v25: Borrowed (1), v18: Borrowed (1), v5: Live (2), v27: Live (2), v10: Live (2), v15: Live (2);
    println!("v22: {:?}",v22);
    println!("v5: {:?}",v5);
    println!("v27: {:?}",v27);
    println!("v10: {:?}",v10);
    println!("v15: {:?}",v15);
}
